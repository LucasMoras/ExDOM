function aum(){

    var imagem = document.getElementById("img");
    var larguraAtual = imagem.width;
    imagem.style.width = (larguraAtual + 10) + "px";
    
}

function dim(){

    var imagem = document.getElementById("img");
    var larguraAtual = imagem.width;
    imagem.style.width = (larguraAtual - 10) + "px";

}